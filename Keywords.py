# Keywords 
# -> python keywords are some reserved words which has special meaning.
# -> keywords cannot be used as an identifiers like variable name or function name.

# In python (keyword) is built-in standard module which helps us to get full list of keywords

import keyword

# List of python keywords
keywords = keyword.kwlist

# Print those keywords
print("Python Keywords : ")
for kw in keywords:
    print(kw)

    

